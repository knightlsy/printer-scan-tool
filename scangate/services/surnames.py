# 仅允许中文与间隔号（·），排除字母 / 数字 / 其它特殊符号
_NAME_RE = re.compile(r"^[\u4e00-\u9fff·\u00b7\u30fb]+$")


def check_realname(name: str) -> dict:
    """校验真实姓名：去前导空格后，首 1~2 字须为有效中国姓氏。

    返回 {"ok": True, "surname": <姓>} 或 {"ok": False, "error": <提示>}
    """
    stripped = (name or "").strip()
    if not stripped:
        return {"ok": False, "error": "姓名不能为空"}
    if len(stripped) < 2:
        return {"ok": False, "error": "姓名至少需 2 个汉字（姓氏 + 名字）"}
    if not _NAME_RE.match(stripped):
        return {"ok": False, "error": "姓名只能由中文及间隔号（·）组成，不能含字母、数字或特殊符号"}

    # 姓氏校验：先匹配复姓（前 2 字），否则匹配单姓（第 1 字）
    first2 = stripped[:2]
    if first2 in COMPOUND_SURNAMES:
        return {"ok": True, "surname": first2}
    if stripped[0] in SINGLE_SURNAMES:
        return {"ok": True, "surname": stripped[0]}
    return {
        "ok": False,
        "error": "请输入真实姓名",
    }
